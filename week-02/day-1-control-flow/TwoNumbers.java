package ControlFlow;

public class TwoNumbers {

    public static void main(String[] args){
        int number1 = 22;
        int number2 = 13;
        double number3 = 13;

        System.out.println(number2 + number1);
        System.out.println(number1 - number2);
        System.out.println(number1 * number2);
        System.out.println(number1 / number3);
        System.out.println(number1 / number2);
        System.out.println(number1 % number2);

    }
}



